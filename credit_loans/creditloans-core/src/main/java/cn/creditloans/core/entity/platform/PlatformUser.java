package cn.creditloans.core.entity.platform;

import java.util.ArrayList;
import java.util.Date;
import java.util.List;

/**
 * 平台用户信息实体
 * @author Austin
 *
 */
public class PlatformUser {
	
	private int id;
	
	private String name;
	
	private String password;
	
	private String email;
	
	private String tel;
	
	private String phone;
	
	private String qq;
	
	/**
	 * 用户状态 0:可用 1:禁用
	 */
	private int status;
	
	/**
	 * 所属部门id
	 */
	private int depId;
	
	private String token;
	
	private Date createTime;
	
	private Date updateTime;
	
	private List<PlatformUserRole> platformUserRoleList = new ArrayList<PlatformUserRole>();
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getQq() {
		return qq;
	}

	public void setQq(String qq) {
		this.qq = qq;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getDepId() {
		return depId;
	}

	public void setDepId(int depId) {
		this.depId = depId;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public List<PlatformUserRole> getPlatformUserRoleList() {
		return platformUserRoleList;
	}

	public void setPlatformUserRoleList(List<PlatformUserRole> platformUserRoleList) {
		this.platformUserRoleList = platformUserRoleList;
	}
}
