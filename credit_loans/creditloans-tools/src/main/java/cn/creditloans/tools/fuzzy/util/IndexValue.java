package cn.creditloans.tools.fuzzy.util;

public class IndexValue {
	/**
	 * 公司名称或者地址名称
	 */
	private String value;
	
	/**
	 * 对应的ID
	 */
	private int id;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
}
